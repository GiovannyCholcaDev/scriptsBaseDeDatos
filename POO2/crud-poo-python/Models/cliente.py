
class Cliente:
     def __init__(self, id_cliente, nombre, apellido, edad):
        self.id_cliente = id_cliente
        self.nombre = nombre
        self.apellido = apellido
        self.edad = edad

def __str__(self):
        return f"ID: {self.id_cliente}, Nombre: {self.nombre}, Apellido: {self.apellido}, Edad: {self.edad}"

    # MÃ©todos para acceder y modificar los atributos
def get_id(self):
        return self.id_cliente

def set_id(self, id_cliente):
        self.id_cliente = id_cliente

def get_nombre(self):
        return self.nombre

def set_nombre(self, nombre):
        self.nombre = nombre

def get_apellido(self):
        return self.apellido

def set_apellido(self, apellido):
        self.apellido = apellido

def get_edad(self):
        return self.edad

def set_edad(self, edad):
        self.edad = edad

